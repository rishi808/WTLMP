                <h2>About Our Club</h2>
                <p>Manchester United Football Club was formed in 1878 as Newton Heath LYR Football Club by the Carriage and Wagon department of the Lancashire and Yorkshire Railway depot at Newton Heath.[1] The team initially played games against other departments and rail companies at their home ground at North Road, but by 1888 the club had become a founding member of The Combination, a regional football league. However, following the league's dissolution before the end of its first season, Newton Heath joined the newly formed Football Alliance, which ran for three seasons before being merged with The Football League. This resulted in the club starting the 1892–93 season in the First Division, by which time it had become independent of the rail company, dropped the "LYR" from its name and moved to a new ground at Bank Street.[1] After just two seasons, the club was relegated to the Second Division.[1]

In January 1902, with debts of £2,670 – equivalent to £260,000 in 2016[2] – the club was served with a winding-up order.[3] Captain Harry Stafford found four local businessmen – including John Henry Davies, who became club president – each willing to invest £500 in return for a direct interest in running the club.[4] As a mark of this fresh start, on 24 April 1902, the club's name was changed to "Manchester United".[5][6] Under Ernest Mangnall, who became club secretary in 1903, the team finished as Second Division runners-up in 1906 and secured promotion to the First Division, which it won in 1908 – the club's first league title. The following season began with victory in the first ever Charity Shield[7] and ended with the club's first FA Cup title. Manchester United moved to a new stadium at Old Trafford in 1910, and won the First Division for the second time in 1911, but at the end of the following season, secretary Mangnall left to join Manchester City.[8]

In 1922, three years after the resumption of football following the First World War, the club was relegated to the Second Division, where it remained until regaining promotion in 1925. Relegated again in 1931, Manchester United became a yo-yo club, achieving its all-time lowest position of 20th place in the Second Division in 1934. Following the death of John Henry Davies in October 1927, the club's finances deteriorated to the extent that Manchester United would likely have gone bankrupt had it not been for an investment of £2,000 in December 1931 by James W. Gibson, who assumed control of the club.[9] In the 1938–39 season – the last year of football before the Second World War – the club finished 14th in the First Division.[9] During the war, the club participated in the Wartime League and the Football League War Cup, but in 1941, Old Trafford was damaged by German bombs and would not be fully repaired until 1949.[10]</p>
